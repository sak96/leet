//! Solution for https://leetcode.com/problems/maximum-path-score-in-a-grid
//! 3742. Maximum Path Score in a Grid

impl Solution {
    pub fn max_path_score(grid: Vec<Vec<i32>>, cost: i32) -> i32 {
        let n = grid[0].len();
        let cost = cost as usize;

        let mut prev = vec![vec![i32::MIN; cost + 1]; n + 1];
        for (i, row) in grid.into_iter().enumerate() {
            let mut current = vec![vec![i32::MIN; cost + 1]; n + 1];
            for (j, val) in row.into_iter().enumerate() {
                for k in 0..=cost {
                    if i == 0 && j == 0 {
                        // grid[0][0] == 0
                        current[j][k] = 0;
                        current[j + 1][k] = 0;
                        continue;
                    }
                    let prev_k = if val != 0 { 1 } else { 0 };
                    current[j + 1][k] = if k < prev_k {
                        -1
                    } else {
                        let max = (current[j][k - prev_k]).max(prev[j + 1][k - prev_k]);
                        if max >= 0 { max + val } else { -1 }
                    };
                }
            }
            std::mem::swap(&mut prev, &mut current);
            for r in current.iter_mut() {
                r.fill(i32::MIN)
            }
        }

        prev.last().unwrap().iter().max().copied().unwrap()
    }
}

