impl Solution {
    const EMPTY: char = '.';

    #[inline]
    #[allow(clippy::needless_range_loop)]
    pub fn is_valid(board: &[Vec<char>], row: usize, col: usize) -> bool {
        let num = board[row][col];
        //check col
        for r in 0..9 {
            if board[r][col] == num && r != row {
                return false;
            }
        }
        // check row
        for c in 0..9 {
            if board[row][c] == num && c != col {
                return false;
            }
        }
        // check square
        let r = (row / 3) * 3;
        for r in r..(r + 3) {
            let c = (col / 3) * 3;
            for c in c..(c + 3) {
                if board[r][c] == num && c != col && r != row {
                    return false;
                }
            }
        }

        true
    }
    pub fn is_valid_sudoku(board: Vec<Vec<char>>) -> bool {
        for row in 0..9 {
            for col in 0..9 {
                if board[row][col] != Self::EMPTY && !Self::is_valid(&board, row, col) {
                    return false;
                }
            }
        }
        true
    }
}

